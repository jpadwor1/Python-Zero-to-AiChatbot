# This is our box (variable) named 'toyBox' and it holds a toy named 'Lego'.
toyBox = "Lego"
print(toyBox)

# We can also have a box named 'jewelryBox' that holds a 'Ring'.
jewelryBox = "Ring"
print(jewelryBox)


# Let's change the content of 'toyBox' from 'Lego' to 'Action Figure'.
toyBox = "Action Figure"
print(toyBox)


