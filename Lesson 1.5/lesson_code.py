# Addition
result = 5 + 3
print(result)  # Outputs: 8

# Subtraction
result = 8 - 3
print(result)  # Outputs: 5

# Multiplication
result = 4 * 2
print(result)  # Outputs: 8

# Division
result = 8 / 2
print(result)  # Outputs: 4.0

# Modulus (Remainder)
result = 9 % 2
print(result)  # Outputs: 1


# Exponentiation
result = 3 ** 2
print(result)  # Outputs: 9

# Floor Division
result = 7 // 2
print(result)  # Outputs: 3


# Combined Operations
result = 5 + 3 * 2
print(result)  # Outputs: 11

# Using Parentheses to change the order
result = (5 + 3) * 2
print(result)  # Outputs: 16
