# Lists with different data types
list1 = ["apple", "banana", "cherry"]  # A list of strings
list2 = [1, 5, 7, 9, 3]  # A list of integers
list3 = [True, False, False]  # A list of boolean values


# Finding the length of our lists
print(len(list1))  # This will print 3
print(len(list2))  # This will print 5
print(len(list3))  # This will print 3

# Adding and removing items from our list
list1.append("dragonfruit")  # Adds "dragonfruit" to the end
list2.remove(7)  # Removes the number 7 from the list
print(list1)
print(list2)
